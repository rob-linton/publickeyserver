		private string GetRandomWord()
		{
			BigInteger rand = BigIntegers.CreateRandomInRange(BigInteger.One, BigInteger.ValueOf(10000), random);

			int r = rand.IntValue;
			return words[r];
		}

		private string GetRarity()
		{
			/*
			 * Rare probabilities
			 * Legendary        1/10000
			 * Epic            10/10000
			 * Rare           100/10000
			 * Uncommon      1000/10000 
			 * Common       10000/10000
			 */

			BigInteger rand = BigIntegers.CreateRandomInRange(BigInteger.One, BigInteger.ValueOf(10000), random);
			int r = rand.IntValue;

			if (r == 1)
				return "legendary";
			else if (r <= 11)
				return "epic";
			else if (r <= 111)
				return "rare";
			else if (r <= 1111)
				return "uncommon";
			else
				return "common";
		}

		public string GetAlias(string origin)
		{
			
			string word1 = GetRandomWord();
			string word2 = GetRandomWord();
			string word3 = GetRandomWord();

			string rarity = "";
			if (word1 == word2 && word1 == word3)
			{
				// legendary
				rarity = "legendary";
			}
			else if (word1 == word2 || word1 == word3 || word2 == word3)
			{
				// epic
				rarity = "epic";
			}
			else
			{
				// get a random rarity
				rarity = GetRarity();
			}

			return $"{word1}-{word2}-{word3}.{origin}";
			//return $"{word1}.{word2}.{word3}.{rarity}.{origin}";
		}
